# 8. Criar um programa em Python que solicite três números ao usuário, utilize estruturas condicionais para determinar o maior entre eles e apresente o resultado.

numero1 = int(input('Digite o numero1: '))

numero2 = int(input('Digite o numero2: '))

numero3 = int(input('Digite o numero3: '))

if numero2 < numero1 > numero3: 
    print(f'O numero {numero1} é o maior')
elif numero1 < numero2 > numero3:
    print(f'O numero {numero2} é o maior')
elif numero1 < numero3 > numero2:
    print(f'O numero {numero3} é o maior')




